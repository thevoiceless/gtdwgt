  end

  # POST /users
  # POST /users.json
  def create
    @user = User.new(params[:user])

    respond_to do |format|
      if @user.save
        sign_in @user
        flash[:success] = "Welcome!"
        format.html { redirect_to @user }
        format.json { render json: @user, status: :created, location: @user }
      else
        format.html { render action: "new" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /users/1
  # PUT /users/1.json
  def update
    # The following line was moved to correct_user, applied via before-filter
    # @user = User.find(params[:id])

    respond_to do |format|
      if @user.update_attributes(params[:user])
        flash[:sucess] = 'Profile updated.'
        # Sign in as part of a successful update
        # The remember token gets reset when the user is saved, which invalidates the userâ€™s session 
        # This is a security feature; any hijacked sessions will automatically expire when the user information is changed
        sign_in @user
        format.html { redirect_to @user }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @user.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /users/1
  # DELETE /users/1.json
  def destroy
    # The following line was moved to correct_user, applied via before-filter
    # @user = User.find(params[:id])
    @user.destroy

    respond_to do |format|
      format.html { redirect_to users_url }
      format.json { head :no_content }
    end
  end

  private

    def signed_in_user
      unless signed_in?
        store_location
        redirect_to signin_url, notice: "Please sign in." unless signed_in?
      end
    end

    # Check if the user is allowed to perform an action
    def correct_user
      @user = User.find(params[:id])
      # current_user?(user) defined in SessionsHelper
      unless current_user?(@user)
        flash[:error] = "You don't have permission to do that."
        redirect_to root_path
      end
    end
end
